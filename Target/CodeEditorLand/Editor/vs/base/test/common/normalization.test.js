import assert from "assert";
import { removeAccents } from "../../common/normalization.js";
import { ensureNoDisposablesAreLeakedInTestSuite } from "./utils.js";
suite("Normalization", () => {
  ensureNoDisposablesAreLeakedInTestSuite();
  test("removeAccents", () => {
    assert.strictEqual(removeAccents("jo\xE0o"), "joao");
    assert.strictEqual(removeAccents("jo\xE1o"), "joao");
    assert.strictEqual(removeAccents("jo\xE2o"), "joao");
    assert.strictEqual(removeAccents("jo\xE4o"), "joao");
    assert.strictEqual(removeAccents("jo\xE3o"), "joao");
    assert.strictEqual(removeAccents("jo\xE5o"), "joao");
    assert.strictEqual(removeAccents("jo\xE5o"), "joao");
    assert.strictEqual(removeAccents("jo\u0101o"), "joao");
    assert.strictEqual(removeAccents("f\xF4o"), "foo");
    assert.strictEqual(removeAccents("f\xF6o"), "foo");
    assert.strictEqual(removeAccents("f\xF2o"), "foo");
    assert.strictEqual(removeAccents("f\xF3o"), "foo");
    assert.strictEqual(removeAccents("f\u014Do"), "foo");
    assert.strictEqual(removeAccents("f\xF5o"), "foo");
    assert.strictEqual(removeAccents("andr\xE8"), "andre");
    assert.strictEqual(removeAccents("andr\xE9"), "andre");
    assert.strictEqual(removeAccents("andr\xEA"), "andre");
    assert.strictEqual(removeAccents("andr\xEB"), "andre");
    assert.strictEqual(removeAccents("andr\u0113"), "andre");
    assert.strictEqual(removeAccents("andr\u0117"), "andre");
    assert.strictEqual(removeAccents("andr\u0119"), "andre");
    assert.strictEqual(removeAccents("hv\xEEc"), "hvic");
    assert.strictEqual(removeAccents("hv\xEFc"), "hvic");
    assert.strictEqual(removeAccents("hv\xEDc"), "hvic");
    assert.strictEqual(removeAccents("hv\u012Bc"), "hvic");
    assert.strictEqual(removeAccents("hv\u012Fc"), "hvic");
    assert.strictEqual(removeAccents("hv\xECc"), "hvic");
    assert.strictEqual(removeAccents("\xFBdo"), "udo");
    assert.strictEqual(removeAccents("\xFCdo"), "udo");
    assert.strictEqual(removeAccents("\xF9do"), "udo");
    assert.strictEqual(removeAccents("\xFAdo"), "udo");
    assert.strictEqual(removeAccents("\u016Bdo"), "udo");
    assert.strictEqual(removeAccents("he\xFF"), "hey");
    assert.strictEqual(removeAccents("gru\u015B"), "grus");
    assert.strictEqual(removeAccents("gru\u0161"), "grus");
    assert.strictEqual(removeAccents("\xE7ool"), "cool");
    assert.strictEqual(removeAccents("\u0107ool"), "cool");
    assert.strictEqual(removeAccents("\u010Dool"), "cool");
    assert.strictEqual(removeAccents("\xF1ice"), "nice");
    assert.strictEqual(removeAccents("\u0144ice"), "nice");
  });
});
